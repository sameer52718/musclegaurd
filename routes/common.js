import { Router } from "express";
import { createCity, createState, getCity, getStates } from "../controllers/common.js";

const router =  Router();

// ----------------------------------------------------STATE ROUTES----------------------------------------------------
// ----------------------------------------------------ADMIN ROUTES----------------------------------------------------
router.post("/state", createState)
// ----------------------------------------------------ADMIN ROUTES----------------------------------------------------
// ----------------------------------------------------PUBLIC ROUTES----------------------------------------------------
router.get("/state/get", getStates)
// ----------------------------------------------------PUBLIC ROUTES----------------------------------------------------

// ----------------------------------------------------STATE ROUTES----------------------------------------------------



// ----------------------------------------------------CITY ROUTES----------------------------------------------------
// ----------------------------------------------------ADMIN ROUTES----------------------------------------------------
router.post("/city", createCity)
// ----------------------------------------------------ADMIN ROUTES----------------------------------------------------
// ----------------------------------------------------PUBLIC ROUTES----------------------------------------------------
router.get("/city/get/:id", getCity)
// ----------------------------------------------------PUBLIC ROUTES----------------------------------------------------

// ----------------------------------------------------CITY ROUTES----------------------------------------------------
export default router